/******************************************************************************
*
* project name:  FAT-Engine
* file name:     tex2hdr.c
* initial date:  05/04/2002
* author:        thomas.nussbaumer@gmx.net
* description:   tool to convert a binary file which contains texture data
*                for the FAT engine into a pretty nice headerfile
*
* the textures in the binary file has to be aligned like the output of
* fixtex64 namely this way:
*
* for grayscales (2 planes):
*
* [tile 1 plane 0 data]
* [tile 1 plane 1 data]
* [tile 2 plane 0 data]
* [tile 2 plane 1 data]
* ..... and so on .....
*
* for mono (1 plane):
*
* [tile 1 data]
* [tile 2 plane 0 data]
* ..... and so on .....
*
* each plane 64x64 pixels large, thus 512 (64*8) large
*
*
* $Id: tex2hdr.c,v 1.2 2002/04/05 15:24:46 tnussb Exp $
*
******************************************************************************/
#include <stdio.h>
#include <string.h>
#include <stdlib.h>

