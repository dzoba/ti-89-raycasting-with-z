//-----------------------------------------------------------------------------
// a few network globals ...
//-----------------------------------------------------------------------------
#define LINK_CONNECTED 1
#define LINK_SERVER    2
#define LINK_SYNC      8
#define LINK_SEND      16
#define LINK_PING      50
#define LINK_PONG      60
#define LINK_RING      70
#define LINK_RONG      80
#define LINK_MSG       90
#define LINK_TIMELIM   5

short         cvars = LINK_SEND;
short         timeout = LINK_TIMELIM;
unsigned char rbuffer[10];

//=============================================================================
// send a ping to the other calc ...
//=============================================================================
void LINKONLYSendPing(unsigned char val) {
    OSWriteLinkBlock(&val,1);
}

//=============================================================================
// send location to the other calc ...
//=============================================================================
void LINKONLYSendData(const unsigned char* data, short num) {
    short count = 0;
    unsigned char sbuffer[num+2];
    sbuffer[0] = LINK_MSG;
    for (count = 0; count < num; count++) {
        sbuffer[count+1] = data[count];
    }
    sbuffer[count+1] = LINK_MSG;
    if (OSLinkTxQueueInquire() < 110) OSLinkReset();
    OSWriteLinkBlock(sbuffer, num+2);
}

//=============================================================================
// send location to the other calc ...
//=============================================================================
unsigned char* LinkConnection(const unsigned char* data, short num) {
    unsigned char* rval = NULL;
    memset(rbuffer, '\0', 10);

    if (!(cvars & LINK_CONNECTED)) {
        OSReadLinkBlock(rbuffer,1);
        if (rbuffer[0] == LINK_PING || rbuffer[0] == LINK_PONG) {
            timeout = LINK_TIMELIM;
            cvars |= LINK_CONNECTED;
            //-------------------------------------------------------------
            // if I got a ping, I am the server, send back a pong to client
            //-------------------------------------------------------------
            if (rbuffer[0] == LINK_PING) {
                LINKONLYSendPing(LINK_PONG);
                LINKONLYSendData(data, num);
                cvars |= LINK_SERVER;
            }
        }
        //-----------------------------------------------------------------
        // send out a ping once and only once at startup ...
        //-----------------------------------------------------------------
        if (!(cvars & LINK_CONNECTED) && (cvars & LINK_SEND)) {
            LINKONLYSendPing(LINK_PING);
            cvars ^= LINK_SEND;
        }
    }
    else if (cvars & LINK_SYNC) {
        //-----------------------------------------------------------------
        // if the link times out, try to sync
        //-----------------------------------------------------------------
        OSReadLinkBlock(rbuffer,1);
        if (rbuffer[0] == LINK_RING || rbuffer[0] == LINK_RONG) {
            timeout = LINK_TIMELIM;
            cvars ^= LINK_SYNC;
            if (rbuffer[0] == LINK_RING) {
                LINKONLYSendPing(LINK_RONG);
            }
            if (cvars & LINK_SERVER) {
                LINKONLYSendData(data, num);
            }
        }
        else {
            LINKONLYSendPing(LINK_RING);
        }
    }
    else {
        //----------------------------------------------------
        // we are connected, read opponent data ...
        //----------------------------------------------------
        OSReadLinkBlock(rbuffer, num+2);
        if (rbuffer[0] == LINK_MSG) {
            timeout = LINK_TIMELIM;
            if (rbuffer[num+1] == LINK_MSG) {
                rval = &rbuffer[1];
            }
            else {
              //-----------------------------------------------------------------
              // we have part of the message, just read the rest and send our
              // location and hope we get all of the message next time ...
              //-----------------------------------------------------------------
              rbuffer[0] = 0;
              do {
                  OSReadLinkBlock(rbuffer, 1);
                  if (rbuffer[0] == LINK_RING) {
                      cvars |= LINK_SYNC;
                      rbuffer[0] = LINK_MSG;
                  }
              } while(rbuffer[0] != LINK_MSG);
            }
            LINKONLYSendData(data, num);
        }
        else if (timeout-- <= 0) {
            cvars |= LINK_SYNC;
        }
    }
    return rval;
}

